#include<iostream>
using namespace std;
int main(){
    int days;
    cin>>days;
    
    switch(days){
        case1:
        cout<<"Monday"<<endl;
        break;

        case2:
        cout<<"Tuesday"<<endl;
        break;

        case3:
        cout<<"Wednesday"<<endl;
        break;

        default:
        cout<<"default case"<<endl;
    }
    return 0;
}